import Foundation


public final class SwiftyLibCalculation {
    
    let name = "SwiftyLibCalculation"

    public func add(a: Int, b: Int) -> Int {
        return a + b
    }

    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
}
